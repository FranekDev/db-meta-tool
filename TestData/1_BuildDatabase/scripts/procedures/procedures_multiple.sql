-- Test: Czy parser obsługuje wiele procedur w jednym pliku

SET TERM ^ ;

CREATE PROCEDURE GET_CUSTOMER_COUNT
RETURNS (CNT INTEGER)
AS
BEGIN
  SELECT COUNT(*) FROM CUSTOMERS INTO :CNT;
END^

CREATE PROCEDURE GET_TOTAL_ORDERS (CUSTOMER_ID BIGINT)
RETURNS (TOTAL_ORDERS INTEGER)
AS
BEGIN
  SELECT COUNT(*)
  FROM ORDERS
  WHERE CUSTOMER_ID = :CUSTOMER_ID
  INTO :TOTAL_ORDERS;
END^

SET TERM ; ^
